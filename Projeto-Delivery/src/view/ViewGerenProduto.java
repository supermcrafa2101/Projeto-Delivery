    public void readJTable() {

        DefaultTableModel modelo = (DefaultTableModel) tbProduto.getModel();
        modelo.setNumRows(0);
        ProdutoDAO produtodao = new ProdutoDAO();

        produtodao.read().forEach((objProduto) -> {
            modelo.addRow(new Object[]{
                objProduto.getID(),
                objProduto.getNome(),
                objProduto.getTipo(),
                objProduto.getPreco()});
        });
    }

