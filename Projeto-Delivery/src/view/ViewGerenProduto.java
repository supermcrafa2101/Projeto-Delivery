    private void btnRemoverProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverProdutoActionPerformed

        if (tbProduto.getSelectedRow() != -1) {

            btnAtualizarProduto.setEnabled(true);

            JFrame frame = new JFrame();
            String[] opcoesSMD = new String[2];
            opcoesSMD[0] = new String("Sim");
            opcoesSMD[1] = new String("Não");

            int escolha = JOptionPane.showOptionDialog(
                    frame.getContentPane(),
                    "O produto selecionado sera removido!!!",
                    "AVISO",
                    0,
                    JOptionPane.WARNING_MESSAGE,
                    null,
                    opcoesSMD,
                    null);

            if (escolha == JOptionPane.YES_OPTION) {

                Produto objProduto = new Produto();
                ProdutoDAO dao = new ProdutoDAO();

                objProduto.setID((int) tbProduto.getValueAt(tbProduto.getSelectedRow(), 0));

                dao.delete(objProduto);
                readJTable();

                tbProduto.clearSelection();
                btnAtualizarProduto.setEnabled(false);
                btnRemoverProduto.setEnabled(false);
                btnAdicionarProduto.setEnabled(true);
                txtNome.setText("");
                txtPreco.setText("");
                tbProduto.clearSelection();

            } else if (escolha == JOptionPane.NO_OPTION) {
                JOptionPane.showMessageDialog(null, "Nada foi alterado.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para remover");
        }
    }//GEN-LAST:event_btnRemoverProdutoActionPerformed

